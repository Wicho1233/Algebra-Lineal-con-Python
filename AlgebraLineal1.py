#Espacios Vectoriales
import numpy as np
#Importacion de la libreria numpy que permite trabajar con arreglos
#Declaracion del primer vector 'a'
a=np.array([-1,9,5,2])
#Declaracion del segundo vector 'b'
b=np.array([1,3,-1,7])
#Manera de realiza la suma de los vectores
